function norm(s) {
  return String(s || "").toLowerCase();
}

function scoreDomain(text, domain) {
  const t = norm(text);
  let score = 0;
  for (const kw of KEYWORDS[domain] || []) {
    if (t.includes(kw.toLowerCase())) score += 1;
  }
  return score;
}

function classify(q) {
  const blob = [
    q.questionText,
    q.explanation,
    ...(q.options || []),
  ].join("\n");

  let best = "General Security Concepts";
  let bestScore = -1;
  for (const d of DOMAINS) {
    const s = scoreDomain(blob, d);
    if (s > bestScore) {
      bestScore = s;
      best = d;
    }
  }
  if (bestScore === 0) {
    // Weak signal: use light keyword hints
    const t = norm(blob);
    if (/\b(phish|attack|malware|threat|exploit|vulnerab)\b/.test(t))
      return "Threats, Vulnerabilities, and Mitigations";
    if (/\b(firewall|network|segment|cloud|architect)\b/.test(t))
      return "Security Architecture";
    if (/\b(incident|log|monitor|backup|patch|siem)\b/.test(t))
      return "Security Operations";
    if (/\b(policy|compliance|audit|governance|vendor)\b/.test(t))
      return "Security Program Management and Oversight";
    if (/\b(privacy|pii|gdpr|consent)\b/.test(t))
      return "Risk Management and Privacy";
    if (/\b(risk register|aro\b|ale\b|impact analysis)\b/.test(t))
      return "Risk Management";
    if (/\b(control type|preventive|detective|corrective)\b/.test(t))
      return "Security Controls";
  }
  return best;
}

const root = path.join(__dirname, "..");
const src = path.join(root, "src", "data", "questions2.json");
const raw = fs.readFileSync(src, "utf8");
const questions = JSON.parse(raw);

let changed = 0;
for (const q of questions) {
  const next = classify(q);
  if (q.category !== next) changed++;
  q.category = next;
}

const counts = {};
for (const q of questions) {
  counts[q.category] = (counts[q.category] || 0) + 1;
}

fs.writeFileSync(src, JSON.stringify(questions, null, 2) + "\n", "utf8");
console.log("Wrote", questions.length, "questions,", changed, "categories updated.");
console.log("Counts:", JSON.stringify(counts, null, 2));
